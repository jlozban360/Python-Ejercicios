# Suma (+)
suma = 3 + 2
print("Suma: 3 + 2 =", suma)

# Resta (-)
resta = 5 - 3
print("Resta: 5 - 3 =", resta)

# Multiplicación (*)
multiplicacion = 4 * 3
print("Multiplicación: 4 * 3 =", multiplicacion)

# División (/)
division = 10 / 2
print("División: 10 / 2 =", division)

# División entera (//)
division_entera = 10 // 3
print("División entera: 10 // 3 =", division_entera)

# Módulo (%) Calcular Resto
modulo = 10 % 3
print("Módulo: 10 % 3 =", modulo)

# Potencia (**)
potencia = 2 ** 3
print("Potencia: 2 ** 3 =", potencia)

# Raíz cuadrada (usando **)
raiz_cuadrada = 16 ** 0.5
print("Raíz cuadrada de 16: 16 ** 0.5 =", raiz_cuadrada)

# Raíz cuadrada (usando math.sqrt)
import math
raiz_cuadrada_math = math.sqrt(16)
print("Raíz cuadrada de 16 usando math.sqrt:", raiz_cuadrada_math)

# Comparación de igualdad (==)
igualdad = (5 == 5)
print("Comparación de igualdad: 5 == 5 =", igualdad)

# Comparación de desigualdad (!=)
desigualdad = (5 != 3)
print("Comparación de desigualdad: 5 != 3 =", desigualdad)

# Mayor que (>)
mayor_que = (7 > 5)
print("Mayor que: 7 > 5 =", mayor_que)

# Menor que (<)
menor_que = (3 < 5)
print("Menor que: 3 < 5 =", menor_que)

# Mayor o igual que (>=)
mayor_igual = (7 >= 5)
print("Mayor o igual que: 7 >= 5 =", mayor_igual)

# Menor o igual que (<=)
menor_igual = (3 <= 5)
print("Menor o igual que: 3 <= 5 =", menor_igual)

# AND lógico (and)
and_logico = (5 > 3 and 3 < 5)
print("AND lógico: 5 > 3 and 3 < 5 =", and_logico)

# OR lógico (or)
or_logico = (5 > 3 or 3 > 5)
print("OR lógico: 5 > 3 or 3 > 5 =", or_logico)

# NOT lógico (not)
not_logico = not(5 > 3)
print("NOT lógico: not(5 > 3) =", not_logico)
